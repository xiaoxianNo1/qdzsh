package com.rushang.pojo;

public class ChangeInfo {
    private String userId;
    private String userCoolName;
    private String userPhone;
    private String userMaill;

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getUserCoolName() {
        return userCoolName;
    }

    public void setUserCoolName(String userCoolName) {
        this.userCoolName = userCoolName;
    }

    public String getUserPhone() {
        return userPhone;
    }

    public void setUserPhone(String userPhone) {
        this.userPhone = userPhone;
    }

    public String getUserMaill() {
        return userMaill;
    }

    public void setUserMaill(String userMaill) {
        this.userMaill = userMaill;
    }
}
